import type { NextPage } from "next";
import Head from "next/head";
import { useRouter } from "next/router";
import { useAppContext } from "../contexts";

const Results: NextPage = () => {
  const { correctAnswers, wrongAnswers, resetGame } = useAppContext();
  const router = useRouter();

  const handleRetry = () => {
    resetGame();
    router.push("/game");
  };

  return (
    <div>
      <Head>
        <title>Project Aldgate</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="border w-max mx-auto mt-12 flex flex-col items-center">
        <h1 className="text-blue-600 text-xl">Results Yo</h1>

        <p>
          Correct answers: <span>{correctAnswers}</span>
        </p>
        <p>
          Wrong answers: <span>{wrongAnswers}</span>
        </p>

        <button
          className="bg-blue-500 p-4 rounded hover:bg-blue-600"
          onClick={handleRetry}
        >
          Try again
        </button>
      </main>
    </div>
  );
};

export default Results;
